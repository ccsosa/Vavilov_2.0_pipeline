# ?????? Main parallel loop ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
message("Processing ", length(species_vec), " species...")

with_progress({
  p <- progressor(along = species_vec)
  
  results <- foreach(
    sp_name        = species_vec,
    .packages      = c("concaveman", "sf", "terra", "data.table"),
    .errorhandling = "pass"
  ) %dopar% {
    
    message(sp_name)
    #only for test
    # sp_name <- species_vec[[1]]
    #concave hull result
    out_file <- file.path(result_conv_dir, paste0(sp_name, "_conc.tif"))
    #convex hull results
    out_file_conv <- file.path(result_convex_dir, paste0(sp_name, "_conv.tif"))
    
    if (file.exists(out_file)) { p(); return(paste0("SKIP (CONCAVE HULL exists): ", sp_name)) }
    if (file.exists(out_file_conv)) { p(); return(paste0("SKIP (CONVEX HULL exists): ", sp_name)) }

    # sp_csv <- file.path(occs_split_dir, paste0(sp_name, ".csv"))
    ############################################################################
    #if is not working the species name for SDM modeling using synonym and loading points for modelling!
    sp_csv <- file.path(occs_path, paste0(sp_name, ".shp"))
    if (!file.exists(sp_csv)) {
      syn_name <- conversion_table$species_from_source[conversion_table$species==sp_name]
      if(length(syn_name)>0 && !is.na(syn_name[1])){
        sp_csv <- file.path(occs_path, paste0(syn_name[1], ".shp"))
      }
    }
    
    if(!file.exists(sp_csv)){p(); return(paste0("SKIP (no occs file): ", sp_name)) }
    ############################################################################
    ############################################################################
    
    #loading shapefiles with points used for modelling1
    # occs_i <- data.table::fread(sp_csv)
    occs_i <- terra::vect(sp_csv)
    if (nrow(occs_i) < 4) { p(); return(paste0("SKIP (< 3 pts): ", sp_name)) }
    
    #Ensuring no polygon topology issues!
    sf::sf_use_s2(FALSE)
    #reading shapefile in sf
    occs_sf <- sf::st_as_sf(occs_i, coords = c("lon", "lat"), crs = 4326)
    #performing concave hull
    conv_i  <- concaveman::concaveman(occs_sf)
    sf::st_crs(conv_i) <- 4326
    
    #intersect with adm0 LATAM
    conv_clipped <- sf::st_intersection(conv_i, adm0_w)
    if (nrow(conv_clipped) == 0) { p(); return(paste0("SKIP (empty intersection): ", sp_name)) }
    conv_clean <- conv_clipped |>
      sf::st_collection_extract("POLYGON") |>
      sf::st_cast("MULTIPOLYGON")
    
    #rasterizing using template and masking to ensure only use LATAM box
    r <- terra::rasterize(terra::vect(conv_clean), tmpl, field = 1, background = 0)
    r <- terra::mask(r, tmpl)
    
    ###convex hull
    hull <- terra::convHull(terra::vect(occs_sf))
    #vertex as points
    hull_pts <- terra::as.points(hull)
    #get maximum distance
    dist_mat <- as.matrix(terra::distance(
      hull_pts,
      unit="m",
      method="haversine"
    ))
    
    #indices with maximum distance
    #idx <-which(dist_mat==max(dist_mat),arr.ind = T)[1,]
    
    #buffer with 10% of maximum distance!
    buffer_width_m <- max(dist_mat * 0.1)
    
    #get buffers!
    buff <- terra::buffer(terra::vect(occs_sf),width=buffer_width_m)
    buff_rast <- terra::rasterize(buff, tmpl,field=1,background=0)
    
    #obtaining a blend of buffer and concave hull
    buff_rast <- buff_rast * tmpl
    
    #getting concave hull + buffer
    r <- terra::resample(r,tmpl,"near")
    r <- r + buff_rast
    # plot(r)
    # r <- terra::rasterize(terra::vect(conv_clipped), tmpl, field = 1, background = NA)
    #only using 1 as raster values
    r[which(r[] > 0)] <- 1
    #removing 0 and replacing as NA
    r[which(r[] == 0)] <- NA
    #saving raster
    terra::writeRaster(r, out_file, overwrite = TRUE)
    
    ############################################################################
    ############################################################################
    #performing for convex hull!
    #rasterizing using template and masking to ensure only use LATAM box
    r2 <- terra::rasterize(hull, tmpl, field = 1, background = 0)
    r2 <- terra::mask(r2, tmpl)
    #obtaining a blend of buffer and convex hull
    #getting concave hull + buffer
    r2 <- terra::resample(r2,tmpl,"near")
    r2 <- r2 + buff_rast
    # r <- terra::rasterize(terra::vect(conv_clipped), tmpl, field = 1, background = NA)
    #only using 1 as raster values
    r2[which(r2[] > 0)] <- 1
    # plot(r2)
    #removing 0 and replacing as NA
    r2[which(r2[] == 0)] <- NA
    #saving raster
    terra::writeRaster(r2, out_file_conv, overwrite = TRUE)
    ############################################################################
    ############################################################################
    # sdm_file <- if (sp_name %in% SDM_result$species) {
    #   file.path(SDM_maps_path,    paste0(sp_name, ".tif"))
    # } else {
    #   file.path(Buffer_maps_path, paste0(sp_name, ".tif"))
    # }
    # if (!file.exists(sdm_file)) { p(); return(paste0("SKIP (no SDM file): ", sp_name)) }
    # 
    # SDM <- terra::rast(sdm_file)
    # SDM <- terra::resample(SDM, r, method = "near")
    # r <- r * SDM

    p()
    return(paste0("OK: ", sp_name))
  }
})

